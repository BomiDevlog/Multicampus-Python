# 산술연산자 + - * / // % **

print(4+10)     # 덧셈 연산
print(4.0-1.0)  # 뺄셈 연산
print(20*20)    # 곱셈 연산
print(11 / 2)   # 실수 나눗셈 연산
print(11//2)    # 정수 나눗셈 연산 : 몫
print(11 % 2)   # 정수 나눗셈 연산 : 나머지
print(4**0.5)   # 거듭제곱 : 4의 1/2승
print(4**5)     # 거듭제곱 : 4의 5승





